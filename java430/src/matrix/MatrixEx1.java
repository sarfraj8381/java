package matrix;

public class MatrixEx1 {

	public static void main(String[] args) {
		int ar[][]= {{12,34,56,78},{12,56,67,88,},{66,77,88,99}};
		for(int i=0;i<ar.length;i++)
		{
			for(int s=0;s<ar.length;s++)
			{
				System.out.print("   "  +ar[i][s]+  "  ");
			}
			{
				System.out.println();
			}
			
		}

}
}

