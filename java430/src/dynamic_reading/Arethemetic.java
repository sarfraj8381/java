 

package dynamic_reading;
import java.util.Scanner;

public class Arethemetic {

	public static void main(String[] args) {
		Scanner s3 = new Scanner(System.in);
		System.out.println("enter number");
		int a= s3.nextInt();
		int b= s3.nextInt();
		System.out.println(a+"+"+b+"="+(a+b));
		System.out.println(a+"-"+b+"="+(a-b));
		System.out.println(a+"*"+b+"="+(a*b));
		System.out.println(a+"/"+b+"="+(a/b));
		System.out.println(a+"%"+b+"="+(a%b));
		

	}

}
